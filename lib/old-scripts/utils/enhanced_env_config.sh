# Function to safely get environment variable with fallback
safe_env() {
    local var_name="$1"
    local fallback="$2"
    local value="${!var_name:-$fallback}"
    echo "$value"
}

# Function to safely get boolean environment variable
safe_bool() {
    local var_name="$1"
    local fallback="$2"
    local value="${!var_name:-$fallback}"
    if [[ "$value" == "true" || "$value" == "1" ]]; then
        echo "true"
    else
        echo "false"
    fi
}

# Function to safely get integer environment variable
safe_int() {
    local var_name="$1"
    local fallback="$2"
    local value="${!var_name:-$fallback}"
    if [[ "$value" =~ ^[0-9]+$ ]]; then
        echo "$value"
    else
        echo "$fallback"
    fi
}

# Function to safely get double environment variable
safe_double() {
    local var_name="$1"
    local fallback="$2"
    local value="${!var_name:-$fallback}"
    if [[ "$value" =~ ^[0-9]+\.?[0-9]*$ ]]; then
        echo "$value"
    else
        echo "$fallback"
    fi
}

