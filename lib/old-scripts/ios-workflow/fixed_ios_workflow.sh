validate_environment() {
    log_info "ðŸ” Validating build environment..."
    
    # Check required variables
    local required_vars=(
        "BUNDLE_ID" "APP_NAME" "VERSION_NAME" "VERSION_CODE"
        "APPLE_TEAM_ID" "CERT_PASSWORD" "PROFILE_URL"
