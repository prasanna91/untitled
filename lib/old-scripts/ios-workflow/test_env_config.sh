# Function to escape strings for Dart
escape_dart_string() {
    local input="$1"
    # Escape backslashes first
    input="${input//\\/\\\\}"
    # Escape quotes
    input="${input//\"/\\\"}"
    # Escape newlines
    input="${input//$'\n'/\\n}"
    # Escape carriage returns
    input="${input//$'\r'/\\r}"
    # Escape tabs
    input="${input//$'\t'/\\t}"
    echo "$input"
}

